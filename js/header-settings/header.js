function showScore() {
	if (document.getElementById('top-profile-menu').style.visibility == 'visible') {
		document.getElementById('top-profile-menu').style.visibility = 'hidden';
	}
	else {
		document.getElementById('top-profile-menu').style.visibility = 'visible';
	}
}

